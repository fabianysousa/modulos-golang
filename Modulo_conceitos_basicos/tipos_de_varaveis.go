a := 10
b := "Hello"
c := 10.33
d := false
e := 'w'

// Imprimir valores

fmt.Printf("%v \n",a)
fmt.Printf("%v \n",b)
fmt.Printf("%v \n",c)
fmt.Printf("%v \n",d)
fmt.Printf("%v \n",e)

// Imprimir tipos

fmt.Printf("%T \n",a) // int
fmt.Printf("%T \n",b) // string
fmt.Printf("%T \n",c) // float64
fmt.Printf("%T \n",e) // bool
fmt.Printf("%T \n",d) // int32
